#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*c;
	size_t	i;

	c = s;
	i = 0;
	if (!n)
		return ;
	while (i < n)
	{
		*(c + i) = 0;
		i++;
	}
}
